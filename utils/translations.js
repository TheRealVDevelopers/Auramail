export const useTranslations = () => {
    const { state } = useAppContext();
    // Ensure that a valid language code is used, defaulting to 'en-US'
    const lang = (Object.keys(translations).includes(state.currentLanguage) ? state.currentLanguage : 'en-US');
    
    // Fallback to English if a key is missing in the current language
    return (key, replacements) => {
        let translation = translations[lang]?.[key] || translations['en-US'][key];
        if (replacements) {
            for (const rKey in replacements) {
                translation = translation.replace(new RegExp(`\\{${rKey}\\}`, 'g'), String(replacements[rKey]));
            }
        }
        return translation;
    };
};